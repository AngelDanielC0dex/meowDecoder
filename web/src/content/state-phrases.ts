/** All 11 user-facing emotional states (MODEL_OUTPUT_CLASSES minus "unknown"). */
export type EmotionalState = Exclude<(typeof VOCALIZATION_CLASSES)[number], "unknown">;

export const EMOTIONAL_STATES: readonly EmotionalState[] = VOCALIZATION_CLASSES.filter(
  (c): c is EmotionalState => c !== "unknown",
);

export const isEmotionalState = (v: string): v is EmotionalState =>
  (EMOTIONAL_STATES as readonly string[]).includes(v);

