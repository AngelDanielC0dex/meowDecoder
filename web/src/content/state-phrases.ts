export function getRandomPhrase(state: EmotionalState, locale: AppLocale): string {
  const entry = STATE_PHRASES.find((e) => e.state === state);
  if (!entry) return "";
  const phrases = entry.i18n[locale];
  return phrases[Math.floor(Math.random() * phrases.length)] ?? "";
}

export function getPhrasesForState(state: EmotionalState, locale: AppLocale): readonly string[] {
  const entry = STATE_PHRASES.find((e) => e.state === state);
  if (!entry) return [];
  return entry.i18n[locale];
}